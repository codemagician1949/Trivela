// Write scenario: 10 VUs posting new campaigns for 30s. Validates the
// SQLite write path and rate limiter behaviour for authenticated writes.

import http from 'k6/http';
import { check, sleep } from 'k6';
import { BASE_URL, defaultThresholds, writeHeaders } from '../lib/config.js';

export const options = {
  scenarios: {
    write_campaigns: {
      executor: 'constant-vus',
      vus: 10,
      duration: '30s',
    },
  },
  thresholds: defaultThresholds,
};

function uniqueSlug() {
  // k6 exposes __VU and __ITER so payloads stay unique across virtual
  // users without an external generator.
  return `loadtest-${__VU}-${__ITER}-${Date.now()}`;
}

export default function () {
  const slug = uniqueSlug();
  const body = JSON.stringify({
    name: `Load test ${slug}`,
    slug,
    description: 'Generated by load-tests/write-campaigns.js',
    rewardPerAction: 1,
    active: true,
  });

  const res = http.post(`${BASE_URL}/api/v1/campaigns`, body, {
    headers: writeHeaders(),
  });

  check(res, {
    'status is 2xx': (r) => r.status >= 200 && r.status < 300,
  });

  sleep(0.5);
}
